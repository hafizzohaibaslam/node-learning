/*
npm -> node package manager

npm enables us to do three things 

1-reuse our own code in other projects
2-use code written by other developers
3-share our own solutions with other developers as well


npm - global command, comes with node
npm --version

loal dependency - use it only in this particular project 

npm i <packageName>

global dependency - use it in any project
npm install -g <packageName>

package.json - manifest file(stores important info about project/package (create package.json in the root, create properties etc)

npm init (step by step ,press enter to skip )

npm init -y (everything default)

*/